package com.snoop.dug.demo.objects;


public class IGORequest {

    private String polNumber;
    private String applicationID;
    private String igoValue;
    private String nigoReason;

    public String getPolNumber() {
        return polNumber;
    }

    public void setPolNumber(String polNumber) {
        this.polNumber = polNumber;
    }

    public String getApplicationID() {
        return applicationID;
    }

    public void setApplicationID(String applicationID) {
        this.applicationID = applicationID;
    }

    public String getIgoValue() {
        return igoValue;
    }

    public void setIgoValue(String igoValue) {
        this.igoValue = igoValue;
    }

    public String getNigoReason() {
        return nigoReason;
    }

    public void setNigoReason(String nigoReason) {
        this.nigoReason = nigoReason;
    }


}
